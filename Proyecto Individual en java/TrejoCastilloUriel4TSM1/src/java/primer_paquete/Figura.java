package primer_paquete;
public abstract class Figura {
  //Son los metodos realizaran la operaciones de obtener Area y Perimetro
    /*
    Los metodos abstractos son declarados pero no implementados esto quiere decir que es un
    metodo que solo se escribe su nombre , parametros y tipo devulto pero no su codigo
    */
  public abstract double obtenrArea();
  
  public abstract double obtenerPerimetro();
  
}
